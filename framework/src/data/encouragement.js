/**
 * ביטויי עידוד עבריים משותפים
 * מאגר מגוון של שבחים ורמזי-ניסיון-חוזר, כדי שלא כל המשחקים יחזרו
 * על אותה מילה יחידה ("כָּל הַכָּבוֹד") בכל תשובה נכונה.
 * כל הביטויים ניטרליים מגדרית (לא פונים ב"אתה/את") כדי להתאים לכל ילד/ה.
 */

/** ביטויי שבח לתשובה נכונה. */
export const PRAISE_PHRASES = [
  'כָּל הַכָּבוֹד',
  'מְצֻיָּן',
  'יֹפִי',
  'נֶהְדָּר',
  'וָאוּ',
  'אֵיזֶה כֵּיף',
];

/**
 * ביטויי עידוד עדינים לניסיון חוזר - בלי אף מילה שלילית.
 * שלושת הראשונים כאן היו במקור מאגר מקומי במשחק קריאת ההברות; רוכזו
 * כאן כדי שגם משחקים אחרים ייהנו מהם במקום לשכפל מחרוזות.
 */
export const RETRY_HINTS = [
  'נַסּוּ שׁוּב, אַתֶּם יְכוֹלִים',
  'כִּמְעַט! הַקְשִׁיבוּ שׁוּב',
  'עוֹד נִסָּיוֹן קָטָן',
  'קְרוֹבִים מְאֹד',
];

/**
 * בחר ביטוי שבח אקראי.
 * @returns {string}
 */
export function randomPraise() {
  return PRAISE_PHRASES[Math.floor(Math.random() * PRAISE_PHRASES.length)];
}

/**
 * בחר רמז עידוד אקראי לניסיון חוזר.
 * @returns {string}
 */
export function randomRetryHint() {
  return RETRY_HINTS[Math.floor(Math.random() * RETRY_HINTS.length)];
}
