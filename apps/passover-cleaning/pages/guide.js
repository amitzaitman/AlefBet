function attachGuideHandlers(container) {
  container.querySelectorAll('.pc-guide-cat-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const catId = btn.dataset.category;
      if (guideState.openCategoryId === catId) {
        guideState.openCategoryId = null;
        guideState.openSectionId = null;
      } else {
        guideState.openCategoryId = catId;
        const cat = categories.find(c => c.id === catId);
        guideState.openSectionId = cat?.sections[0]?.id ?? null;
      }
      container.innerHTML = buildGuideHTML();
      attachGuideHandlers(container);
    });
  });

  container.querySelectorAll('.pc-guide-section-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const secId = btn.dataset.section;
      guideState.openSectionId = guideState.openSectionId === secId ? null : secId;
      container.innerHTML = buildGuideHTML();
      attachGuideHandlers(container);
    });
  });
}

export function renderGuide(container) {
  container.innerHTML = buildGuideHTML();
  attachGuideHandlers(container);
}
